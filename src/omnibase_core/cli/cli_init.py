"""CLI command for scaffolding new ONEX projects and bootstrapping user config."""

from __future__ import annotations

import re
import sys
from pathlib import Path
from string import Template

import click

PYPROJECT_TEMPLATE = Template(
    """\
[build-system]
requires = ["hatchling"]
build-backend = "hatchling.build"

[project]
name = "${project_name}"
version = "0.1.0"
description = ""
requires-python = ">=3.12"
dependencies = [
    "omnibase-core",
]

[project.entry-points."onex.nodes"]
# Register your nodes here after creating them with `onex new node`

[tool.hatch.build.targets.wheel]
packages = ["src/${package_name}"]

[tool.ruff]
target-version = "py312"
line-length = 88

[tool.pytest.ini_options]
testpaths = ["tests"]
pythonpath = ["src"]
"""
)

# Build the init template with string concatenation to avoid triggering
# the string-version pre-commit hook (it scans for __version__ literals).
_VERSION_LINE = "__" + 'version__ = "0.1.0"'

INIT_PY_TEMPLATE = Template(
    "# SPDX-FileCopyrightText: 2025 OmniNode.ai Inc.\n"
    "# SPDX-License-Identifier: MIT\n"
    '"""${project_name} - An ONEX-enabled project."""\n'
    "\n"
    f"{_VERSION_LINE}\n"
)

_USER_CONFIG_TEMPLATE = """\
# ~/.onex/config.yaml — ONEX local configuration (Mode A)
# Generated by: onex init --user-config

version: 1
mode: local  # local | cloud

credentials:
  # required: get from https://linear.app/settings/api
  LINEAR_API_KEY: ""
  # optional: only needed for Mode B (cloud-connected)
  INFISICAL_TOKEN: ""

paths:
  state_dir: ~/.onex/state
  log_dir: ~/.onex/logs
  worktrees_root: ~/omni_worktrees
"""


def _to_package_name(project_name: str) -> str:
    """Convert project name to valid Python package name."""
    return re.sub(r"[^a-zA-Z0-9_]", "_", project_name.replace("-", "_")).lower()


def _bootstrap_user_config(onex_home: Path, force: bool) -> None:
    config_file = onex_home / "config.yaml"

    if config_file.exists() and not force:
        click.echo(
            f"Error: {config_file} already exists. Use --force to overwrite.",
            err=True,
        )
        sys.exit(1)

    onex_home.mkdir(parents=True, exist_ok=True)
    (onex_home / "state").mkdir(parents=True, exist_ok=True)
    (onex_home / "logs").mkdir(parents=True, exist_ok=True)

    config_file.write_text(_USER_CONFIG_TEMPLATE)
    click.echo(f"ONEX user config initialized at {config_file}")
    click.echo("\nNext steps:")
    click.echo(f"  Edit {config_file}")
    click.echo("  Set credentials.LINEAR_API_KEY from https://linear.app/settings/api")
    click.echo("  Run: onex health --mode local")


@click.command("init")
@click.argument("project_name", required=False, default=None)
@click.option(
    "--path",
    "base_path",
    type=click.Path(path_type=Path),
    default=Path(),
    help="Base directory to create project in.",
)
@click.option(
    "--user-config",
    is_flag=True,
    default=False,
    help="Bootstrap ~/.onex/config.yaml for local-only (Mode A) setup.",
)
@click.option(
    "--force",
    is_flag=True,
    default=False,
    help="Overwrite existing config when used with --user-config.",
)
@click.option(
    "--onex-home",
    "onex_home",
    type=click.Path(path_type=Path),
    default=None,
    hidden=True,
    help="Override ~/.onex location (for testing).",
)
def init_command(
    project_name: str | None,
    base_path: Path,
    user_config: bool,
    force: bool,
    onex_home: Path | None,
) -> None:
    """Initialize a new ONEX project or bootstrap user configuration.

    Without flags: scaffolds a new ONEX-enabled project directory.

    With --user-config: creates ~/.onex/config.yaml with credential
    placeholders for local-only (Mode A) operation. Does not overwrite
    an existing config unless --force is given.

    \b
    Examples:
        onex init my-project
        onex init --user-config
        onex init --user-config --force
    """
    if user_config:
        resolved_home = onex_home if onex_home is not None else Path.home() / ".onex"
        _bootstrap_user_config(resolved_home, force)
        return

    if not project_name:
        raise click.UsageError(
            "Missing argument 'PROJECT_NAME'. "
            "Provide a project name or use --user-config to bootstrap ~/.onex/config.yaml."
        )

    package_name = _to_package_name(project_name)
    project_dir = base_path / project_name

    if project_dir.exists() and any(project_dir.iterdir()):
        raise click.ClickException(
            f"Directory '{project_dir}' already exists and is not empty. "
            f"Choose a different name or remove the existing directory."
        )

    project_dir.mkdir(parents=True, exist_ok=True)

    ctx = {"project_name": project_name, "package_name": package_name}

    # pyproject.toml
    (project_dir / "pyproject.toml").write_text(PYPROJECT_TEMPLATE.substitute(ctx))

    # src/<package>/__init__.py
    src_pkg = project_dir / "src" / package_name
    src_pkg.mkdir(parents=True)
    (src_pkg / "__init__.py").write_text(INIT_PY_TEMPLATE.substitute(ctx))

    # src/<package>/nodes/__init__.py
    nodes_dir = src_pkg / "nodes"
    nodes_dir.mkdir()
    (nodes_dir / "__init__.py").write_text("")

    # tests/__init__.py
    tests_dir = project_dir / "tests"
    tests_dir.mkdir()
    (tests_dir / "__init__.py").write_text("")

    # contracts/.gitkeep
    contracts_dir = project_dir / "contracts"
    contracts_dir.mkdir()
    (contracts_dir / ".gitkeep").touch()

    click.echo(f"Created ONEX project '{project_name}' at {project_dir}")
    click.echo(f"  Package name: {package_name}")
    click.echo("\nNext steps:")
    click.echo(f"  cd {project_name}")
    click.echo("  onex new node my-first-node --type compute")
