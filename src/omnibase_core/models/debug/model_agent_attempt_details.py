"""
Model for agent attempt details in debug intelligence system.

This model represents detailed information about agent execution attempts
for learning and improvement purposes.
"""

from pydantic import BaseModel, Field


class ModelAgentAttemptDetails(BaseModel):
    """Model for agent attempt details."""

    code_generated: str | None = Field(
        default=None,
        description="Code generated during the attempt",
    )
    commands_executed: list[str] = Field(
        default_factory=list,
        description="List of commands executed",
    )
    files_modified: list[str] = Field(
        default_factory=list,
        description="List of files that were modified",
    )
    tools_used: list[str] = Field(
        default_factory=list,
        description="List of tools used during execution",
    )
    execution_time_seconds: float | None = Field(
        default=None,
        description="Time taken to execute the attempt",
    )
    memory_usage_mb: float | None = Field(
        default=None,
        description="Memory usage during execution",
    )
    output_generated: str | None = Field(
        default=None,
        description="Output generated by the attempt",
    )
    environment_info: dict[str, str] = Field(
        default_factory=dict,
        description="Environment information during execution",
    )
    dependencies_used: list[str] = Field(
        default_factory=list,
        description="List of dependencies used",
    )
    approach_description: str | None = Field(
        default=None,
        description="Description of the approach taken",
    )
